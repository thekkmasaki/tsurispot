export function getAreaGuideBySlug(slug: string): AreaGuide | undefined {
  return areaGuides.find((g) => g.slug === slug);
}

export function getRelatedAreaGuides(slug: string): AreaGuide[] {
  const guide = getAreaGuideBySlug(slug);
  if (!guide) return [];
  return guide.relatedSlugs
    .map((s) => getAreaGuideBySlug(s))
    .filter((g): g is AreaGuide => g !== undefined);
}
