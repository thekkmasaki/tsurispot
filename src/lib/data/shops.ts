/** 2点間の距離をkmで概算（Haversine簡易版） */
function distanceKm(lat1: number, lng1: number, lat2: number, lng2: number): number {
  const R = 6371;
  const dLat = ((lat2 - lat1) * Math.PI) / 180;
  const dLng = ((lng2 - lng1) * Math.PI) / 180;
  const a =
    Math.sin(dLat / 2) ** 2 +
    Math.cos((lat1 * Math.PI) / 180) *
      Math.cos((lat2 * Math.PI) / 180) *
      Math.sin(dLng / 2) ** 2;
  return R * 2 * Math.atan2(Math.sqrt(a), Math.sqrt(1 - a));
}

export interface NearbyShopWithDistance {
  shop: TackleShop;
  distanceKm: number;
}

export function getNearbyShops(
  lat: number,
  lng: number,
  limit: number = 3
): TackleShop[] {
  return getNearbyShopsWithDistance(lat, lng, limit).map((item) => item.shop);
}

export function getNearbyShopsWithDistance(
  lat: number,
  lng: number,
  limit: number = 3,
  maxDistanceKm: number = 50
): NearbyShopWithDistance[] {
  return [...tackleShops]
    .map((shop) => ({
      shop,
      distanceKm: distanceKm(lat, lng, shop.latitude, shop.longitude),
    }))
    .filter((item) => item.distanceKm <= maxDistanceKm)
    .sort((a, b) => a.distanceKm - b.distanceKm)
    .slice(0, limit);
}

export function getShopsForSpot(spotSlug: string): TackleShop[] {
  return tackleShops.filter((shop) =>
    shop.nearbySpotSlugs.includes(spotSlug)
  );
}
