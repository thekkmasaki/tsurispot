import { describe, it, expect } from "vitest";
import { checkNgWords } from "../moderation";

// ヘルパー: NG判定されることを検証
const expectNg = (texts: string[]) => expect(checkNgWords(texts).ok).toBe(false);
// ヘルパー: セーフ判定されることを検証
const expectOk = (texts: string[]) => expect(checkNgWords(texts).ok).toBe(true);

describe("checkNgWords", () => {
  // ============================================
  // 正常系（セーフ）
  // ============================================
  describe("正常な釣果報告はセーフ", () => {
    it("基本的な投稿", () => {
      expectOk(["釣りキチ太郎", "アジ", "朝マヅメにサビキで20匹釣れました！"]);
    });

    it("空テキスト", () => {
      expectOk(["", "", ""]);
    });

    it("絵文字入り", () => {
      expectOk(["太郎🎣", "アジ", "大漁でした！🐟🐟🐟"]);
    });

    it("長めのコメント", () => {
      expectOk(["磯釣り太郎", "メジナ", "朝5時から粘って3枚。水温16度でやや渋かったが、オキアミ改でなんとか。潮止まりからの下げで食いが立った"]);
    });
  });

  // ============================================
  // 釣り用語の誤検出回避
  // ============================================
  describe("釣り用語はセーフ", () => {
    it("「ゴミが多い」（現場報告）", () => expectOk(["太郎", "アジ", "堤防にゴミが多いので持ち帰りましょう"]));
    it("「カスミアジ」（魚名）", () => expectOk(["太郎", "カスミアジ", "テスト"]));
    it("「ボケで釣った」（餌名）", () => expectOk(["太郎", "チヌ", "ボケで釣りました"]));
    it("「PEライン」（釣り糸）", () => expectOk(["太郎", "シーバス", "PEライン1号で"]));
    it("「LINEで友達に報告」（勧誘でない）", () => expectOk(["太郎", "アジ", "LINEで友達に報告"]));
    it("「障害者用トイレあり」（バリアフリー）", () => expectOk(["太郎", "アジ", "障害者用トイレあり"]));
    it("「雑魚」（小魚の意味）", () => expectOk(["太郎", "雑魚", "小さいのがたくさん"]));
    it("「根がかり」", () => expectOk(["太郎", "カサゴ", "根がかりに注意"]));
    it("「シナノユキマス」", () => expectOk(["太郎", "シナノユキマス", "よく釣れた"]));
    it("「ヘチ釣り」", () => expectOk(["太郎", "チヌ", "ヘチ釣りで3枚"]));
  });

  // ============================================
  // 暴言・侮辱の検出
  // ============================================
  describe("暴言・侮辱", () => {
    it("死ね", () => expectNg(["テスト", "死ね", "コメント"]));
    it("しね（ひらがな）", () => expectNg(["テスト", "アジ", "しね"]));
    it("氏ね（ネットスラング）", () => expectNg(["テスト", "アジ", "氏ね"]));
    it("タヒね（回避表現）", () => expectNg(["テスト", "アジ", "タヒね"]));
    it("殺す", () => expectNg(["テスト", "殺すぞ", "テスト"]));
    it("馬鹿", () => expectNg(["テスト", "アジ", "馬鹿じゃないの"]));
    it("クソ", () => expectNg(["テスト", "アジ", "クソスポット"]));
    it("クズ", () => expectNg(["テスト", "アジ", "クズ"]));
    it("ブス", () => expectNg(["テスト", "アジ", "ブスが来てた"]));
    it("ハゲ", () => expectNg(["テスト", "アジ", "ハゲ"]));
    it("ゴキブリ", () => expectNg(["テスト", "アジ", "ゴキブリみたいなやつ"]));
    it("消えろ", () => expectNg(["テスト", "アジ", "消えろ"]));
    it("頭おかしい", () => expectNg(["テスト", "アジ", "頭おかしい"]));
    it("キチガイ", () => expectNg(["テスト", "アジ", "キチガイ"]));
    it("低能", () => expectNg(["テスト", "アジ", "低能"]));
  });

  // ============================================
  // ネットスラング・隠語
  // ============================================
  describe("ネットスラング", () => {
    it("ガイジ", () => expectNg(["ガイジ", "アジ", "テスト"]));
    it("池沼", () => expectNg(["テスト", "アジ", "池沼"]));
    it("チー牛", () => expectNg(["テスト", "アジ", "チー牛"]));
    it("こどおじ", () => expectNg(["テスト", "アジ", "こどおじ"]));
    it("底辺", () => expectNg(["テスト", "アジ", "底辺"]));
    it("ナマポ", () => expectNg(["テスト", "アジ", "ナマポ"]));
  });

  // ============================================
  // 差別語
  // ============================================
  describe("差別語", () => {
    it("チョン", () => expectNg(["テスト", "アジ", "チョン"]));
    it("カタワ", () => expectNg(["テスト", "アジ", "カタワ"]));
    it("部落民", () => expectNg(["テスト", "アジ", "部落民"]));
  });

  // ============================================
  // 性的表現
  // ============================================
  describe("性的表現", () => {
    it("セックス", () => expectNg(["テスト", "アジ", "セックス"]));
    it("SEX（英字）", () => expectNg(["テスト", "アジ", "SEX"]));
    it("ポルノ", () => expectNg(["テスト", "アジ", "ポルノ"]));
    it("デリヘル", () => expectNg(["テスト", "アジ", "デリヘル"]));
    it("痴漢", () => expectNg(["テスト", "アジ", "痴漢"]));
    it("盗撮", () => expectNg(["テスト", "アジ", "盗撮"]));
    it("エロ動画", () => expectNg(["テスト", "アジ", "エロ動画"]));
  });

  // ============================================
  // 犯罪・違法
  // ============================================
  describe("犯罪・違法", () => {
    it("覚醒剤", () => expectNg(["テスト", "アジ", "覚醒剤"]));
    it("大麻", () => expectNg(["テスト", "アジ", "大麻"]));
    it("シャブ", () => expectNg(["テスト", "アジ", "シャブ"]));
    it("コカイン", () => expectNg(["テスト", "アジ", "コカイン"]));
    it("闇バイト", () => expectNg(["テスト", "アジ", "闇バイト"]));
    it("爆弾", () => expectNg(["テスト", "アジ", "爆弾"]));
    it("児童ポルノ", () => expectNg(["テスト", "アジ", "児童ポルノ"]));
    it("ロリコン", () => expectNg(["テスト", "アジ", "ロリコン"]));
    it("援助交際", () => expectNg(["テスト", "アジ", "援助交際"]));
    it("パパ活", () => expectNg(["テスト", "アジ", "パパ活"]));
    it("自殺しろ", () => expectNg(["テスト", "アジ", "自殺しろ"]));
  });

  // ============================================
  // 個人攻撃・晒し
  // ============================================
  describe("晒し・個人攻撃", () => {
    it("晒す", () => expectNg(["テスト", "アジ", "晒す"]));
    it("特定した", () => expectNg(["テスト", "アジ", "特定した"]));
    it("住所特定", () => expectNg(["テスト", "アジ", "住所特定"]));
    it("凸する", () => expectNg(["テスト", "アジ", "凸する"]));
  });

  // ============================================
  // 正規表現パターン（回避表現）
  // ============================================
  describe("回避表現の検出", () => {
    it("し ね（スペース挿入）", () => expectNg(["テスト", "アジ", "し ね"]));
    it("死 ね（スペース挿入）", () => expectNg(["テスト", "アジ", "死 ね"]));
    it("こ ろ す（スペース挿入）", () => expectNg(["テスト", "アジ", "こ ろ す"]));
    it("fuck（英語暴言）", () => expectNg(["テスト", "アジ", "fuck you"]));
    it("shit（英語暴言）", () => expectNg(["テスト", "アジ", "shit"]));
    it("bitch（英語暴言）", () => expectNg(["テスト", "アジ", "bitch"]));
    it("fuuuck（引き伸ばし）", () => expectNg(["テスト", "アジ", "fuuuck"]));
  });

  // ============================================
  // 連絡先誘導
  // ============================================
  describe("連絡先誘導", () => {
    it("LINE ID交換", () => expectNg(["太郎", "アジ", "LINE交換しよう"]));
    it("LINE ID教え", () => expectNg(["太郎", "アジ", "LINE ID教えて"]));
    it("LINE追加", () => expectNg(["太郎", "アジ", "LINE追加して"]));
    it("DMして", () => expectNg(["太郎", "アジ", "DMしてください"]));
    it("カカオ", () => expectNg(["太郎", "アジ", "カカオで連絡"]));
    it("インスタID交換", () => expectNg(["太郎", "アジ", "インスタID交換しよう"]));
  });

  // ============================================
  // スパム検出
  // ============================================
  describe("スパムパターン", () => {
    it("URL", () => {
      const result = checkNgWords(["太郎", "アジ", "https://spam.example.com"]);
      expect(result.ok).toBe(false);
      expect(result.reason).toContain("URL");
    });

    it("メールアドレス", () => {
      const result = checkNgWords(["太郎", "アジ", "test@example.com"]);
      expect(result.ok).toBe(false);
    });

    it("電話番号", () => {
      const result = checkNgWords(["太郎", "アジ", "090-1234-5678"]);
      expect(result.ok).toBe(false);
    });
  });

  // ============================================
  // 荒らし検出
  // ============================================
  describe("荒らしパターン", () => {
    it("連続同一文字（5文字以上）", () => {
      const result = checkNgWords(["太郎", "アジ", "ああああああああ"]);
      expect(result.ok).toBe(false);
      expect(result.reason).toContain("パターン");
    });

    it("4文字繰り返しはセーフ", () => {
      expectOk(["太郎", "アジ", "ああああ良い天気"]);
    });
  });
});
