const dataDir = path.join(__dirname, 'src', 'lib', 'data');
let totalFixes = 0;
let totalErrors = 0;

for (const [file, oldLat, oldLng, newLat, newLng, name] of fixes) {
  const filePath = path.join(dataDir, file);
  let content = fs.readFileSync(filePath, 'utf-8');

  // Find and replace the latitude
  const latPattern = `latitude: ${oldLat}`;
  const lngPattern = `longitude: ${oldLng}`;

  // Count occurrences to ensure we're making the right fix
  const latCount = (content.match(new RegExp(latPattern.replace('.', '\\.'), 'g')) || []).length;
  const lngCount = (content.match(new RegExp(lngPattern.replace('.', '\\.'), 'g')) || []).length;

  if (latCount === 0) {
    console.log(`WARNING: ${name} - latitude ${oldLat} not found in ${file}`);
    totalErrors++;
    continue;
  }

  if (latCount > 1) {
    console.log(`WARNING: ${name} - latitude ${oldLat} found ${latCount} times in ${file}, skipping`);
    totalErrors++;
    continue;
  }

  content = content.replace(
    new RegExp(`latitude: ${oldLat.replace('.', '\\.')}(,?)`),
    `latitude: ${newLat}$1`
  );
  content = content.replace(
    new RegExp(`longitude: ${oldLng.replace('.', '\\.')}(,?)`),
    `longitude: ${newLng}$1`
  );

  fs.writeFileSync(filePath, content, 'utf-8');
  console.log(`FIXED: ${name} in ${file}: ${oldLat},${oldLng} -> ${newLat},${newLng}`);
  totalFixes++;
}

console.log(`\nDone! Fixed: ${totalFixes}, Errors: ${totalErrors}`);
